function team(
  group: string,
  name: string,
  abbr: string,
  fifaRank: number,
  elo: number,
  attack: number,
  defense: number,
  form: number,
  injuries: number,
  host = false
): SeedTeam {
  return {
    id: abbr.toLowerCase().replace(/[^a-z0-9]+/g, "-"),
    name,
    abbr,
    group,
    fifaRank,
    elo,
    attack,
    defense,
    form,
    injuries,
    host,
    color: groupColors[group]
  };
}

